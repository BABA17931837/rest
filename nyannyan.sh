BRIGHT_RED="\033[1;31m"
NO_COLOR="\033[0m"
CLEAR="\033[2J\033[H"

while true; do
    printf "${CLEAR}${BRIGHT_RED}"
    printf " /\\_/\\  /\\_/\\ \n"
    printf "(o.o  )(o.o  )\n"
    printf " > ^ <  > ^ < \n"
    printf "${NO_COLOR}"
    sleep 0.8

    printf "${CLEAR}${BRIGHT_RED}"
    printf " /\\_/\\  /\\_/\\ \n"
    printf "( -.-) (  o.o)\n"
    printf " > ^ <  > ^ < \n"
    printf "${NO_COLOR}"
    sleep 0.8

    printf "${CLEAR}${BRIGHT_RED}"
    printf " /\\_/\\  /\\_/\\ \n"
    printf "(  o.o)(o.o  )\n"
    printf " > ^ <  > ^ < \n"
    printf "${NO_COLOR}"
    sleep 0.8

    printf "${CLEAR}${BRIGHT_RED}"
    printf " /\\_/\\  /\\_/\\ \n"
    printf "( >.< )( >.< )\n"
    printf " > ^ <  > ^ < \n"
    printf "${NO_COLOR}"
    sleep 0.8

    clear
done
